    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil = insert.simpan("INSERT INTO `tbdinasluar`(`Tanggal`, `IdKaryawan`, `UangDinas`, `Keterangan`) VALUES ('" + FDateF.datetostr(JDTanggal.getDate(), "yyyy-MM-dd") + "',(SELECT `IdKaryawan` FROM `tbmkaryawan` WHERE `NamaKaryawan` = '" + JCNamaKaryawan.getSelectedItem() + "'),'" + JTUangDinas.getInt() + "','" + JTAKeterangan.getText() + "')", "Dinas Luar", this);
            if (berhasil) {
                UpdateAll update = new UpdateAll();
                berhasil = update.Ubah("UPDATE `tbabsen` SET `Hadir` = " + JCBHadir.isSelected() + ", `StatusUangMakan` = " + JCBPotongUangMakan.isSelected() + " WHERE `Tanggal` = '" + FDateF.datetostr(JDTanggal.getDate(), "yyyy-MM-dd") + "' AND `IdKaryawan` = (SELECT `IdKaryawan` FROM `tbmkaryawan` WHERE `NamaKaryawan` = '" + JCNamaKaryawan.getSelectedItem() + "')", "Dinas Luar", this);
                if (berhasil) {
                    if (tutup) {
                        GlobalVar.Var.tambahDinasLuar.dispose();
                        GlobalVar.Var.tambahDinasLuar = null;
                    } else {
                        JCNamaKaryawan.requestFocus();
                    }
                    if (GlobalVar.Var.listDinasLuar != null) {
                        GlobalVar.Var.listDinasLuar.load();
                    }
                }
            }
        }
    }

    void ubahData() {
        if (checkInput()) {
            Update update = new Update();
            Boolean berhasil = update.Ubah("UPDATE `tbdinasluar` SET `Tanggal`='" + FDateF.datetostr(JDTanggal.getDate(), "yyyy-MM-dd") + "',`IdKaryawan`=(SELECT `IdKaryawan` FROM `tbmkaryawan` WHERE `NamaKaryawan` = '" + JCNamaKaryawan.getSelectedItem() + "'),`UangDinas`='" + JTUangDinas.getInt() + "',`Keterangan`='" + JTAKeterangan.getText() + "' WHERE `IdPartai` = " + IdEdit, "Dinas Luar", this);
            if (berhasil) {
                UpdateAll updateAll = new UpdateAll();
                berhasil = updateAll.Ubah("UPDATE `tbabsen` SET `Hadir` = " + JCBHadir.isSelected() + ", `StatusUangMakan` = " + JCBPotongUangMakan.isSelected() + " WHERE `Tanggal` = '" + FDateF.datetostr(JDTanggal.getDate(), "yyyy-MM-dd") + "' AND `IdKaryawan` = (SELECT `IdKaryawan` FROM `tbmkaryawan` WHERE `NamaKaryawan` = '" + JCNamaKaryawan.getSelectedItem() + "')", "Dinas Luar", this);
                if (berhasil) {
                    dispose();
                }
            }
            if (GlobalVar.Var.listDinasLuar != null) {
                GlobalVar.Var.listDinasLuar.load();
            }
        }
    }

