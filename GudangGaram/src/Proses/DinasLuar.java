    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdDinasLuar` as 'ID', DATE_FORMAT(a.`Tanggal`,'%d-%m-%Y') as 'Tanggal', `NamaKaryawan` as 'Nama', `UangDinas` as 'Uang Dinas', a.`Keterangan`, `Hadir`, `StatusUangMakan` FROM `tbdinasluar`a JOIN `tbmkaryawan`b ON a.`IdKaryawan`=b.`IdKaryawan` JOIN `tbabsen`c ON a.`IdKaryawan`=c.`IdKaryawan` WHERE `IdDinas` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JDTanggal.setDate(FDateF.strtodate(list.get(1), "dd-MM-yyyy"));
        JCNamaKaryawan.setSelectedItem(list.get(2));
        JTUangDinas.setText(list.get(3));
        JTAKeterangan.setText(list.get(4));
        JCBHadir.setSelected(list.get(5).equals("1"));
        JCBPotongUangMakan.setSelected(list.get(6).equals("1"));
    }

    Boolean checkInput() {
        if (JDTanggal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Tanggal Dinas Tidak Boleh Kosong");
            return false;
        } else if (JTUangDinas.getInt() == 0) {
            JOptionPane.showMessageDialog(null, "Uang Dinas Tidak Boleh Kosong");
            return false;
        } else if (JTAKeterangan.getText().replace(" ", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Keterangan Dinas Tidak Boleh Kosong");
            return false;
        } else {
            return true;
        }
    }

