    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdPemakaianLain` as 'ID', DATE_FORMAT(`Tanggal`,'%d-%m-%Y') as 'Tanggal', `NamaBarangLain` as 'Nama Barang', `Jumlah`, a.`Keterangan` FROM `tbpemakaianlain`a JOIN `tbmbaranglain`b ON a.`IdBarangLain`=b.`IdBarangLain` WHERE `IdPemakaianLain` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JDTanggal.setDate(FDateF.strtodate(list.get(1), "dd-MM-yyyy"));
        JCNamaBarang.setSelectedItem(list.get(2));
        JTJumlah.setText(list.get(3).split("\\.")[0]);
        JTKoma.setText(list.get(3).split("\\.")[1]);
        JTAKeterangan.setText(list.get(4));
    }

    Boolean checkInput() {
        if (JDTanggal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Tanggal Terima Tidak Boleh Kosong");
            return false;
        } else if (JCNamaBarang.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Nama Barang");
            return false;
        } else if (JTJumlah.getInt() == 0 && JTKoma.getText().replace("0", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Jumlah Tidak Boleh Kosong");
            return false;
        } else {
            return true;
        }
    }

