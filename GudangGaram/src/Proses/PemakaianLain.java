    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil = false;
            berhasil = insert.simpan("INSERT INTO `tbpemakaianlain`(`Tanggal`, `IdBarangLain`, `Jumlah`, `Keterangan`) VALUES ('" + FDateF.datetostr(JDTanggal.getDate(), "yyyy-MM-dd") + "',(SELECT `IdBarangLain` FROM `tbmbaranglain` WHERE `NamaBarangLain` = '" + JCNamaBarang.getSelectedItem() + "'),'" + JTJumlah.getInt() + "." + JTKoma.getText() + "','" + JTAKeterangan.getText() + "')", "Pemakaian Barang Lain", this
            );
            if (berhasil) {
                if (GlobalVar.Var.listPemakaianLain != null) {
                    GlobalVar.Var.listPemakaianLain.load();
                }
                if (tutup) {
                    GlobalVar.Var.tambahPemakaianLain.dispose();
                    GlobalVar.Var.tambahPemakaianLain = null;
                } else {
                    JTJumlah.setText("0");
                    JTKoma.setText("00");
                    JTAKeterangan.setText("");
                }
            }
        }
    }

