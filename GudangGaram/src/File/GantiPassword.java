    boolean checkInput() {
        if (new String(JTPasswordBaru.getPassword()).equals(new String(JTKonfirmasiPassword.getPassword()))) {
            return Checkpassword();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal Ubah Password \n Password Lama Tidak Sama Dengan Password Baru !!! ");
            return false;
        }
    }

    boolean Checkpassword() {
        DRunSelctOne dRunSelctOne = new LSubProces.DRunSelctOne();
        dRunSelctOne.seterorm("Gagal Ubah Passsword");
        dRunSelctOne.setQuerynolimit("SELECT `Username`, `Password`, `Level` FROM `tblogin` WHERE Username='" + GlobalVar.Var.Username + "' and `Password` = '" + getMD5(new String(JTPasswordLama.getPassword())) + "'");
        ArrayList<String> list = dRunSelctOne.excute();
        try {
            //////System.out.println(list.get(1));
            if (list.get(0) != null) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Ubah Passsword \n password Lama Salah");
                return false;
            }
        } catch (Exception e) {
            Eror.LogEror.SaveEror(e);
            //////System.out.println(e);
            JOptionPane.showMessageDialog(this, "Gagal Ubah Password");
            return false;
        }
    }

