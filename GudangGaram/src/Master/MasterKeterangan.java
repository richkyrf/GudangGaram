    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdKeterangan` AS `ID`, `Keterangan`, `Satuan`, `Rupiah` FROM `tbmketerangan` WHERE `IdKeterangan` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JTKeterangan.setText(list.get(1));
        JTSatuan.setText(list.get(2));
        JTRupiah.setText(list.get(3));
    }

    Boolean checkInput() {
        if (JTKeterangan.getText().replace(" ", "").equals("")) {
            JOptionPane.showMessageDialog(this, "Keterangan Tidak Boleh Kosong");
            JTKeterangan.requestFocus();
            return false;
        } else if (JTSatuan.getText().replace(" ", "").equals("")) {
            JOptionPane.showMessageDialog(this, "Satuan Tidak Boleh Kosong");
            JTSatuan.requestFocus();
            return false;
        } else if (JTRupiah.getText().replace("0", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Rupiah Tidak Boleh Kosong");
            JTRupiah.requestFocus();
            return false;
        } else {
            return true;
        }
    }

