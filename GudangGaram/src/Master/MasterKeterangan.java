    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil = insert.simpan("INSERT INTO `tbmketerangan`(`Keterangan`, `Satuan`, `Rupiah`) VALUES ('" + JTKeterangan.getText() + "','" + JTSatuan.getText() + "','" + JTRupiah.getInt() + "')", "Keterangan", this);
            if (berhasil) {
                if (GlobalVar.Var.rekapPenggajianHarian != null) {
                    GlobalVar.Var.rekapPenggajianHarian.JCKeterangan.load("SELECT '-- Pilih Keterangan --' as 'Keterangan' UNION ALL SELECT `Keterangan` FROM `tbmketerangan` ORDER BY `Keterangan` ASC");
                    GlobalVar.Var.rekapPenggajianHarian.JCKeterangan.setSelectedItem(JTKeterangan.getText());
                    GlobalVar.Var.rekapPenggajianHarian.JCKeterangan.requestFocus();
                }
                if (GlobalVar.Var.listKeterangan != null) {
                    GlobalVar.Var.listKeterangan.load();
                }
                if (tutup) {
                    GlobalVar.Var.tambahKeterangan.dispose();
                    GlobalVar.Var.tambahKeterangan = null;
                } else {
                    JTKeterangan.setText("");
                    JTSatuan.setText("");
                    JTRupiah.setText("0");
                    JTKeterangan.requestFocus();
                }
            }
        }
    }

    void ubahData() {
        if (checkInput()) {
            Update update = new Update();
            Boolean berhasil = update.Ubah("UPDATE `tbmketerangan` SET `Keterangan`='" + JTKeterangan.getText() + "',`Satuan`='" + JTSatuan.getText() + "',`Rupiah`='" + JTRupiah.getInt() + "' WHERE `IdKeterangan` = " + IdEdit, "Keterangan", this);
            if (berhasil) {
                dispose();
            }
            if (GlobalVar.Var.listKeterangan != null) {
                GlobalVar.Var.listKeterangan.load();
            }
        }
    }

